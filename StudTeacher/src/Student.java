public class Student {

    int rollno;
    String s_name, s_email;

    int getRollno()
    {
        return rollno;
    }

    String getS_name()
    {
        return s_name;
    }

    String getS_email()
    {
        return s_email;
    }

    void setRollno(int rollno)
    {
        this.rollno = rollno;
    }

    void setS_name(String name)
    {
        this.s_name = name;
    }

    void setS_email(String email)
    {
        this.s_email = email;
    }
}